const translations: Record<AppLanguage, Record<I18nKey, string>> = {
  'zh-CN': zh,
  'en-US': en
};

export function normalizeLanguage(value: unknown): AppLanguage {
  return value === 'en-US' || value === 'zh-CN' ? value : defaultLanguage;
}

export function t(language: AppLanguage, key: I18nKey, values: Record<string, string | number> = {}): string {
  const template = translations[normalizeLanguage(language)][key] ?? translations[defaultLanguage][key];
  return Object.entries(values).reduce(
    (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
    template
  );
}
